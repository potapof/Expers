set -euo pipefail

BASE="${BASE_URL:-https://expers.ru}"
echo "=== Seed test data for $BASE ==="

# ------------------------------------------------------------------
# 1. Регистрация пользователей
# ------------------------------------------------------------------
echo "--- Registering users ---"

register() {
  local name="$1" email="$2" pass="$3"
  curl -k -s -X POST "$BASE/api/auth/register" \
    -H "Content-Type: application/json" \
    -d "{\"name\":\"$name\",\"email\":\"$email\",\"password\":\"$pass\"}" \
    -o /dev/null -w "%{http_code}" || echo "FAIL"
}

R1=$(register "Тестовый Читатель" "reader@test.expers.ru" "test123456")
echo "  reader@test.expers.ru: $R1"

A1=$(register "Тестовый Автор" "author@test.expers.ru" "test123456")
echo "  author@test.expers.ru: $A1"

A2=$(register "Тестовый Админ" "admin@test.expers.ru" "admin123456")
echo "  admin@test.expers.ru: $A2"

# ------------------------------------------------------------------
# 2. Получение токенов
# ------------------------------------------------------------------
echo "--- Getting tokens ---"

login() {
  local email="$1" pass="$2"
  curl -k -s -X POST "$BASE/api/auth/login" \
    -H "Content-Type: application/json" \
    -d "{\"email\":\"$email\",\"password\":\"$pass\"}" | jq -r '.token // empty'
}

READER_TOKEN=$(login "reader@test.expers.ru" "test123456")
AUTHOR_TOKEN=$(login "author@test.expers.ru" "test123456")
echo "  reader token: ${READER_TOKEN:0:20}..."
echo "  author token: ${AUTHOR_TOKEN:0:20}..."

if [ -z "$AUTHOR_TOKEN" ]; then
  echo "ERROR: failed to get author token"
  exit 1
fi

# ------------------------------------------------------------------
# 3. Создание статей
# ------------------------------------------------------------------
echo "--- Creating articles ---"

create_article() {
  local file="$1"
  local code
  code=$(curl -k -s -X POST "$BASE/api/articles" \
    -H "Content-Type: application/json" \
    -H "Authorization: Bearer $AUTHOR_TOKEN" \
    -d @"$file" -o /dev/null -w "%{http_code}")
  echo "  $file: HTTP $code"
}

