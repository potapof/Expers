export function getRelatedArticles(
  articleId: string,
  limit?: number
): ArticleContent[] {
  const article = getArticleContentById(articleId);
  if (!article) return [];
  const linkedIds = article.crossLinks.map((l) => l.articleId);
  const linked = articleContents.filter((a) => linkedIds.includes(a.id));
  return typeof limit === "number" ? linked.slice(0, limit) : linked;
}

