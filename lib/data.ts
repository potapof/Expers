export interface Category {
  id: string;
  name: string;
}

export interface Subsection {
  id: string;
  name: string;
  categories: Category[];
}

export interface Industry {
  id: string;
  name: string;
  subsections: Subsection[];
}

export interface Author {
  id: string;
  name: string;
  avatar?: string;
  bio?: string;
  expertise?: string[];
  credentials?: string[];
  socialLinks?: { platform: string; url: string }[];
}

export interface Article {
  id: string;
  title: string;
  slug?: string;
  description: string;
  author: Author;
  date: string;
  industryId: string;
  categoryId: string;
  readTime: string;
}

export interface KeyFact {
  icon: string;
  text: string;
}

export interface HowToStep {
  title: string;
  description: string;
}

export interface FaqItem {
  question: string;
  answer: string;
}

export interface ReviewItem {
  author: string;
  role: string;
  text: string;
  rating: number;
}

export interface SourceItem {
  title: string;
  url: string;
}

export interface ArticleContent extends Article {
  slug?: string;
  content: string;
  tldr: string;
  keyFacts: KeyFact[];
  definition: string;
  featuredSnippet: { question: string; answer: string };
  problemSolutionResult: {
    problem: string;
    solution: string;
    result: string;
  };
  howTo: HowToStep[];
  faq: FaqItem[];
  reviews: ReviewItem[];
  aggregateRating: {
    ratingValue: number;
    reviewCount: number;
    bestRating: number;
  };
  methodology: string;
  sources: SourceItem[];
  crossLinks: { articleId: string; title: string; industryId: string }[];
}

