function sanitize(input: string): string {
  return input.replace(/[#*_{}\[\]<>`~|]/g, "\\$&");
}

export function generateTemplate(authorName: string, topic?: string): string {
  const safeTopic = topic ? sanitize(topic) : "";
  const safeAuthor = sanitize(authorName || "Эксперт");
  const topicLine = safeTopic
    ? `\n## Тема статьи\n${safeTopic}\n`
    : "\n## Тема статьи\n[Укажите тему статьи здесь]\n";

  const parts = [SYSTEM_PROMPT, topicLine];

  for (const iter of TEMPLATE_ITERATIONS) {
    const header = `## Итерация ${iter.iteration}: ${iter.title}`;
    const wizardInfo = `**${iter.wizardStepLabel}**`;
    const prompt = iter.prompt;
    const example = `### Пример вывода\n${iter.exampleOutput}`;

    parts.push(`${header}\n\n${wizardInfo}\n\n${prompt}\n\n${example}`);
  }

  parts.push(
    `\n---\n\n## Инструкция для пользователя\n\n` +
      `1. Скопируйте промпт итерации 1 и отправьте в ChatGPT / Claude / Perplexity\n` +
      `2. Полученный ответ вставьте в форму импорта на странице /cabinet/import\n` +
      `3. Нажмите «Сохранить» и перейдите к следующей итерации\n` +
      `4. Повторите для всех 12 итераций\n` +
      `5. После завершения всех 12 — статья будет создана автоматически\n\n` +
      `**Важно:** для итераций 1-4 ИИ выводит значения-заглушки.\n` +
      `Вы сможете выбрать реальную отрасль, категорию и экспертизу в редакторе после импорта.\n\n` +
      `Автор: ${safeAuthor}\n` +
      `Дата генерации шаблона: ${new Date().toLocaleDateString("ru-RU")}`
  );

  return parts.join("\n\n---\n\n");
}
