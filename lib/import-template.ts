/* ================================================================
   SELF-ANNOTATION: Санитайзер для защиты от prompt injection.
   Экранирует Markdown-управляющие символы в пользовательском вводе.
   ================================================================ */
function sanitize(input: string): string {
  return input.replace(/[#*_{}\[\]<>`~|]/g, "\\$&");
}

/* ================================================================
   SELF-ANNOTATION: Генератор полного .md файла шаблона.
   Принимает имя автора и тему статьи, возвращает готовый Markdown.
   Используется в GET /api/templates/article для скачивания.
   ================================================================ */
export function generateTemplate(authorName: string, topic?: string): string {
  const safeTopic = topic ? sanitize(topic) : "";
  const safeAuthor = sanitize(authorName || "Эксперт");
  const topicLine = safeTopic
    ? `\n## Тема статьи\n${safeTopic}\n`
    : "\n## Тема статьи\n[Укажите тему статьи здесь]\n";

  const parts = [SYSTEM_PROMPT, topicLine];

  parts.push(
    `\n## ВАЖНО: демо-данные\n\n` +
      `Разделы каталога (отрасль, подсектор, категория), области экспертизы ` +
      `и кросс-ссылки уже заполнены демо-значениями. ИИ их НЕ генерирует.\n` +
      `Вы сможете выбрать реальную отрасль, категорию и другие параметры ` +
      `вручную в редакторе после импорта статьи.\n`
  );

  for (const iter of TEMPLATE_ITERATIONS) {
    const header = `## Итерация ${iter.iteration}: ${iter.title}`;
    const wizardInfo = `**${iter.wizardStepLabel}**`;
    const prompt = iter.prompt;
    const example = `**Пример вывода:**\n\n${iter.exampleOutput}`;

    parts.push(`${header}\n\n${wizardInfo}\n\n${prompt}\n\n${example}`);
  }

  parts.push(
    `\n---\n\n## Инструкция для пользователя\n\n` +
      `1. Отправьте этот шаблон в ChatGPT, Claude или Perplexity\n` +
      `2. ИИ будет выдавать итерации по очереди. После каждой — пауза:\n` +
      `   «Проверьте итерацию N и напишите Продолжить»\n` +
      `3. Когда все 8 итераций готовы — скопируйте ВЕСЬ диалог\n` +
      `4. Вставьте его в поле импорта на странице /cabinet/import\n` +
      `5. Нажмите «Импортировать» — статья будет создана автоматически\n\n` +
      `**Поля отрасли, категории, экспертизы и кросс-ссылок заполнены демо-данными.**\n` +
      `Вы сможете выбрать реальные значения в редакторе после импорта.\n\n` +
      `Автор: ${safeAuthor}\n` +
      `Дата генерации шаблона: ${new Date().toLocaleDateString("ru-RU")}`
  );

  return parts.join("\n\n---\n\n");
}
