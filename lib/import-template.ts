/* ================================================================
   SELF-ANNOTATION: Генератор промпта из SectionSpec.
   ================================================================ */
function buildContentPrompt(
  iterationTitle: string,
  specs: SectionSpec[]
): string {
  const balanceRules = `## Правило баланса «текст ↔ изображение»
800x400 картинка = 400px высоты ≈ 1500 символов текста
1200x600 картинка = 600px — image-only, подпись 100-200 символов
text-only блоки: 2500-3500 символов

`;

  const sectionLines = specs.map((s) => {
    const designLabel =
      s.design === "image-only"
        ? "image-only (картинка на всю ширину)"
        : s.design === "image-right"
          ? "image-right (текст слева, картинка справа)"
          : s.design === "image-left"
            ? "image-left (картинка слева, текст справа)"
            : s.design === "table"
              ? "text-only + таблица в Markdown"
              : "text-only (только текст)";

    const dims = s.imageDims ? `, ${s.imageDims}` : "";
    const url = s.imageUrl ? `\n   ${s.imageUrl}` : "";
    const chars = `${s.minChars}-${s.maxChars}`;

    return `${s.number}. ${designLabel}${dims} — ${s.titleGuidance}${url}
   Текст: ${chars} символов. О чём: ${s.contentGuidance}`;
  });

  const formatBlock = `## Формат вывода
### sectionCount
${specs.length}

${specs
  .map((s) => {
    const imgLine =
      s.design !== "text-only"
        ? `### section${s.number}Картинка\n<URL изображения>\n`
        : "";
    return `### section${s.number}Заголовок\n<Заголовок секции>\n### section${s.number}Дизайн\n${s.design}\n${imgLine}### section${s.number}Текст\n<${s.minChars}-${s.maxChars} символов>`;
  })
  .join("\n\n")}`;

  return `## Задание
Создай ${specs.length} секций «${iterationTitle}».
Секции — небольшие блоки. Соблюдай диапазоны символов — это критично для вёрстки.

${balanceRules}## Секции (строго по порядку)

${sectionLines.join("\n\n")}

${formatBlock}`;
}

function buildGeoFormatBlock(fields: { key: string; desc: string }[]): string {
  return fields.map((f) => `### ${f.key}\n<${f.desc}>`).join("\n\n");
}

